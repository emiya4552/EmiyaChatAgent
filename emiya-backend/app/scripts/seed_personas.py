async def seed():
    async with AsyncSessionLocal() as session:
        result = await session.execute(
            select(Persona).where(Persona.is_template == True)
        )
        existing = result.scalars().all()
        if existing:
            print(f"清除 {len(existing)} 条旧模板...")
            for p in existing:
                await session.delete(p)
            await session.commit()

        all_templates = AI_TEMPLATES + USER_TEMPLATES
        for t in all_templates:
            persona = Persona(is_template=True, user_id=None, source="template", **t)
            session.add(persona)

        await session.commit()
        print(f"插入 {len(AI_TEMPLATES)} 个 AI 角色 + {len(USER_TEMPLATES)} 个用户角色 = {len(all_templates)} 条模板。")


if __name__ == "__main__":
    asyncio.run(seed())
